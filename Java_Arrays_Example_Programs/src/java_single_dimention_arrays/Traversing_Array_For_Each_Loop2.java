package java_single_dimention_arrays;

public class Traversing_Array_For_Each_Loop2 {
	public static void main(String[] args) {
		int arr[]= {1,2,3,4};//declaration, instantiation and initialization
		
		//traversing through the for each loop
		for(int a:arr) {
			System.out.println(a);
		}
	}
}
